#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int	i;
	int j;

	i = ft_strlen(s1) + 1;
	j = 0;
	while (s2[j])
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	return (s1);
}
