#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	unsigned int	i;
	unsigned int	j;

	i = ft_strlen(s1) + 1;
	j = 0;
	while (j <= n)
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	return (s1);
}
